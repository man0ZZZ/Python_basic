id_all=[]
first_name_all=[]
last_name_all=[]
salary_all=[]
department_all=[]

## 1 ADD employee function

def case_employee(id,fname,lname,sal,dep):
  if id in id_all:
    print('ID already exists')
  else:
    id_all.append(id)
    first_name_all.append(fname)
    last_name_all.append(lname)
    salary_all.append(sal)
    department_all.append(dep)
    print('Employee with id: ',id,'added SUCCESSFULLY',)

## 2 REMOVE employee by ID

def remove_emp_id(id_to_remove):
  try:
    index_to_remove=id_all.index(id_to_remove)
    id_all.pop(index_to_remove)
    first_name_all.pop(index_to_remove)
    last_name_all.pop(index_to_remove)
    salary_all.pop(index_to_remove)
    department_all.pop(index_to_remove)
    print('Employee of id: ',id,'removed SUCCESSFULLY')
  except:
    print('ID does not exist...Please try valid id...')


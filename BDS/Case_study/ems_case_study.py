## 3 Remove employee by Name:
def remove_emp_by_name(f_name,l_name):
  all_record=[]
  for k in range(len(first_name_all)):
    if first_name_all[k]==f_name:
      if last_name_all[k]==l_name:
        all_record.append(k)
  
  if len(all_record)==1:
    id_to_remove=id_all[all_record[0]]
    remove_emp_id(id_to_remove)
  
  elif len(all_record)>1:
    print('Multiple records of same name found')
    for c in all_record:
      print('ID:-',id_all[c])
    id_to_remove=int(input('Please enter employee Id to remove: '))
    remove_emp_id(id_to_remove)
  
  else:
    print('Wrong input received, try with correct first name and last name of employee')    


